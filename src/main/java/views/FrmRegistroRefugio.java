    private void guardarRefugio(){
        
        try{
            // 1. Tomar datos de los campos
            String nombre = txtNombre.getText().trim();
            String capacidadStr = txtCapacidad.getText().trim(); // Lo tomamos como String para validar
            String ubicacion = txtUbicacion.getText().trim();
            String responsable = txtResponsable.getText().trim();

            // 2. Ejecutar validación completa (Campos vacíos, Regex y números)
            if (!validarCampos()) {
                return; // Si validarCampos() muestra un error y devuelve false, nos detenemos aquí
            }

            // 3. Si pasó la validación, convertimos la capacidad a int con seguridad
            int capacidad = Integer.parseInt(capacidadStr);

            // 4. Lógica para GUARDAR (Insertar)
            if (btnRegistrar.getText().equals("REGISTRAR")) {
                boolean exito = rController.agregarRefugio(nombre, capacidad, ubicacion, responsable);
                if (exito) {
                    JOptionPane.showMessageDialog(this, "Refugio agregado con éxito");
                    cargarTablaRefugios(); // Refrescar tu tabla o lista
                } else {
                    JOptionPane.showMessageDialog(this, "Error al guardar el refugio", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } 

            // 5. Lógica para ACTUALIZAR
            else if (btnRegistrar.getText().equals("ACTUALIZAR")) {
                int id = Integer.parseInt(txtID.getText().trim()); // Asegúrate de tener el ID para actualizar

                boolean exito = rController.editarRefugio(id, nombre, capacidad, ubicacion, responsable);
                if (exito) {
                    JOptionPane.showMessageDialog(this, "Refugio actualizado correctamente.");
                    cargarTablaRefugios();
                } else {
                    JOptionPane.showMessageDialog(this, "Error al actualizar el refugio.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }

            limpiarFormulario(); // Vaciar los TXT después de la operación exitosa
        } catch (Exception ex){
            JOptionPane.showMessageDialog(
                    this,
                    "Error: " + ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }
        
    }
    
    private boolean validarCampos() {
        // Validar que nada esté vacío (usando .isEmpty())
        if (txtNombre.getText().trim().isEmpty() || 
            txtCapacidad.getText().trim().isEmpty() || 
            txtUbicacion.getText().trim().isEmpty() || 
            txtResponsable.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
            return false; 
        }

        // 2. Validar que la capacidad sea un número entero positivo
        String capTexto = txtCapacidad.getText().trim();
        if (!capTexto.matches("\\d+")) { // Regex: solo dígitos
            JOptionPane.showMessageDialog(this, "La capacidad debe ser un número entero (sin decimales).");
            return false;
        }

        try {
            int cap = Integer.parseInt(capTexto);
            if (cap <= 0) {
                JOptionPane.showMessageDialog(this, "La capacidad debe ser mayor a cero.");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El número de capacidad es demasiado grande.");
            return false;
        }

        // 3. Opcional: Validar longitud mínima de nombres
        if (txtNombre.getText().trim().length() < 3) {
            JOptionPane.showMessageDialog(this, "El nombre del refugio es muy corto.");
            return false;
        }
        
        // Validar que el nombre del responsable solo tenga letras
        String resp = txtResponsable.getText().trim();
        if (!resp.matches("^[a-zA-ZáéíóúÁÉÍÓÚñÑ ]+$")) {
            JOptionPane.showMessageDialog(this, "El nombre del responsable solo puede contener letras.");
            return false;
        }

        // Puedes hacer lo mismo para el nombre del refugio si quieres ser estricto
        String nombreRef = txtNombre.getText().trim();
        if (!nombreRef.matches("^[a-zA-ZáéíóúÁÉÍÓÚñÑ 0-9]+$")) { // Aquí sí permito números por si es "Refugio 1"
            JOptionPane.showMessageDialog(this, "El nombre del refugio tiene caracteres no permitidos.");
            return false;   
        }
        
        return true; // Si llegó hasta aquí, ¡todo está perfecto!
    }
    
