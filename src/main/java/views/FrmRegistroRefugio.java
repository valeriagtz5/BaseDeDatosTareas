    private void eliminarRefugio() {
        try {
            // Obtener el ID del campo
            if (txtID.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Selecciona un refugio de la tabla primero.");
                return;
            }
            int id = Integer.parseInt(txtID.getText());

            // Preguntar
            int confirm = JOptionPane.showConfirmDialog(this, 
                    "¿Está seguro de querer eliminar este refugio?",
                    "Confirmar Eliminación", 
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.WARNING_MESSAGE);

            if (confirm == JOptionPane.YES_OPTION) {

                try {
                    // Llamada al controlador
                    boolean exito = rController.eliminarRefugio(id);

                    if (exito) {
                        JOptionPane.showMessageDialog(this, "Refugio eliminado con éxito");
                        cargarTablaRefugios();     
                        limpiarFormulario(); 
                    } else {
                        JOptionPane.showMessageDialog(this, 
                                "No se encontró el refugio a eliminar", 
                                "Error", 
                                JOptionPane.ERROR_MESSAGE);
                    }

                } catch (SQLException e) {
                    // Si el refugio tiene animales asignados
                    JOptionPane.showMessageDialog(this,
                            "No se puede eliminar: el refugio aún tiene animales asociados.",
                            "Restricción de Base de Datos",
                            JOptionPane.WARNING_MESSAGE);
                }   
            }       
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error al procesar la eliminación: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
        
        
    }
    
    public void buscar(){
        String nombre = txtFiltro.getText().trim();
        if(nombre.isEmpty()){
            cargarTablaRefugios();
        } else {
            tblRefugios.setModel(rController.obtenerTablaRefugiosFiltroNombre(nombre));
        }
    }
